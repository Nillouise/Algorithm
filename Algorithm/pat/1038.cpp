LL get(LL len)
{
    LL ans=0;
    for(LL i=1;i<=len;i++)
    {
        LL t = i*pow(9ll,len-i);
        LL c =1;
        for(int j=len;j>len-i;j--)
        {
            c*=j;
            c/=len-j+1;
        }
        // cout<<"c"<<c<<endl;
        ans+=t*c;
    }
    return ans;
}


